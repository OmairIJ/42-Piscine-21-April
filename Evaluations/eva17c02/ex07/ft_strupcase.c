#include <unistd.h>

/*
** A-> Z (65 -> 90)
** a -> z (97 -> 122)
*/

char	*ft_strupcase(char *str)
{
	int	ct;

	ct = 0;
	while (*(str + ct) != '\0')
	{
		if (*(str + ct) >= 97 && *(str + ct) <= 122)
		{
			*(str + ct) = *(str + ct) - 32;
		}
		ct++;
	}
	return (str);
}
