void	ft_putstr(int *tab, int size)
{
	int i;
	int t;

	i = 0;
	while (i < size / 2)
	{
		t = tab[i];
		*(tab + i) = *(tab + (size - i - 1));
		*(tab + (size - i - 1)) = t;
		i++;
	}
}
